<h3>内線一括管理</h3>
主に初期設定時にCSVファイルで内線情報を管理する際に使用します。<br>
ファイルのアップロードとダウンロードはそれぞれ、リストア/バックアップのページからバックアップファイルと同じ方法で行ってください。<br>
なお、使用するファイルの名前はextconf-で始まり、拡張子は.csvとしてください。
CSVファイルで内線の設定情報を管理し投入することができます。
<h4>現在情報読み込み</h4>
ABSに現在設定されている情報を読み込み、このページに反映します。<br>
内線情報をファイルに保存するにはまずこの操作を行ってください。
<h4>内線情報保存</h4>
このページに表示されている情報をCSVファイルとして保存します。保存はABS側に行われますのでローカルに保存したい場合にはバックアップページからダウンロードします。
<h4>内線設定更新</h4>
このページに表示されている情報でABSの内線S設定情報を更新します。<br>
更新する際にはCID(cidname)を書き換えるか、MACアドレスを書き換えるかも指定(チェック)した上で、内線情報を上書きにチェックしてからボタンをクリックしてください。<br>
なお、CSVファイルで内線情報を一括更新しようとした場合、CID名の更新とMACアドレスは以前の情報に紐付いた情報を完全には置換できない場合がありますので注意してください(番号≠ピア名のため)。余計な情報が残る場合にはコマンドでDBを削除してください。
<h4>設定ファイル</h4>
ABSに保存されている内線設定ファイル(CSV)の一覧です。内容をこのページに反映するには[読み込み]をクリックします。読み込んでも"内線設定更新"を実行しない限り、実際のABS設定には反映されません。<br>
<br>
注:CSVファイルの内容チェックは"ザル"です。所定の順番で項目を記述しないとABSの設定はおかしくなります。
