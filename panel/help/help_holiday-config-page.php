<h3>休日管理</h3>
祝日・休日を自動判定するための日付を管理します。<br>
公的な祝日・休日は内閣府のデータ(CSV)を取得してきて登録することができます。<br>
<h4>祝日・休日管理</h4>
チェックを付けた日を休日(非営業日)とします。
<h4>独自データ追加</h4>
公的な祝・休日以外の日付を登録します。
<h4>個別データ削除</h4>
登録されている日付から特定のものを削除します。
<h4>祝日・休日情報再取得</h4>
指定したURL(デフォルトは内閣府)から祝・休日を取得します。この操作を行うと全てのデータが初期化されるので注意してください。<br>
データがうまく取得できない、登録されないことがありますので不具合がある場合にはサポートを依頼してください。
