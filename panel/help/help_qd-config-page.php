<h3>短縮ダイヤル管理</h3>
短縮(クイック)ダイヤルを管理します。クィックダイヤルは特番*000～*299で発信できる機能です。
<h4>クイック時プレフィクス設定</h4>
クィックダイヤルで発信する際のプレフィクスを設定します。
<h3>クィックダイヤル追加登録</h3>
<h4>短縮番号</h4>
短縮に設定する番号を000～299の数字で設定します。
<h4>電話番号</h4>
電話番号をハイフンやカッコを除いた数字のみで入力します。
<h4>名前</h4>
名称を入力します。CALLERID(name)に置換されます。ここで登録した情報は発信者名管理にも反映されます。

