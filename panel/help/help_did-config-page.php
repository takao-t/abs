<h3>ダイヤルイン着信設定</h3>
ABSの着信は『番号ベース』で制御します。着信先番号とその鳴動先を設定することで、ダイヤルイン着信となります。
<h4>着信番号</h4>
着信番号を指定します。ハイフンやカッコ等を除いた数字のみで指定してください。'any'を設定すると、全ての番号に着信する着信先となります。
<h4>着信先</h4>
着信先の内線またはグループをプルダウンで選択します。
<h4>ダイヤルイン時鳴動パターン</h4>
ダイヤルイン着信で使用する鳴動パターンを設定します。
<h4>着信時外線捕捉プレフィクス付加</h4>
着信時に外線捕捉用のプレフィクスを付加する方法を設定します。ダイヤルインで着信した場合にはOGP1で設定されたプレフィクスが付加されます。キー着信時にはキー発信用のプレフィクスが付加されます。
