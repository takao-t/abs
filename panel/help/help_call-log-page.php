<h3>着信記録管理</h3>
着信記録の設定を管理します。AsteriskのCDRとは別に管理しています。<br>
<h4>記録表示</h4>
<B>表示対象履歴：</B>ローテーションを行った場合に古い履歴を参照する際に設定します。数字は世代です。現在の履歴の直前の履歴が"1"となります。<BR>
履歴を参照するには着信履歴または着信拒否の履歴ページを開きます。
<h4>記録設定</h4>
<B>着信記録：</B>着信履歴を記録する場合にはチェックして[設定]クリックします。<BR>
<B>着信拒否記録：</B>着信拒否の履歴を記録する場合にはチェックして[設定]クリックします。<BR>
<h4>ログ・ローテーションの実行</h4>
着信履歴は通常の履歴、拒否履歴ともに同一のDBに記録されます。このログが大きくなりすぎることを防止するためのローテーションを実行します。保存は9世代まで行われます。<BR>
実行するにはチェックしてから[実行]をクリックします。<BR>
