<h3>着信履歴</h3>
着信履歴を一覧表示します。新しいものが上になります。<br>
注意：着信はすべて着信として扱います。着信拒否している場合でもこの履歴には現れます。<br>
　　　着信拒否の記録のみを見たい場合には着信拒否履歴を使用してください。<br>
　　　非通知着信を拒否している場合にはこの履歴には現れません。<br>
<B>ID</B>：データベース内部での一意の番号<br>
<B>日時</B>：着信した日時<br>
<B>発信者番号</B>：相手の番号. 赤文字は着信拒否登録されているもの.<br>
番号のリンクをクリックすると発信者名登録を行うことができます。<br>
<B>着信先</B>：着信先の番号<br>
<B>拒否登録</B>：この番号を着信拒否リストに登録したい場合にはチェックボックスをオンにして[登録]をクリックします<br>
