<h3>発信設定</h3>
発信に関する各種情報を設定します。発信にはプレフィックス発信またはキーシステム発信が使えます。
<h3>プレフィクス発信</h3>
プレフィクスで発信する際の情報を指定します。プレフィックス発信を行うためにはプレフィクスでキーを捕捉するか、トランクを捕捉(ノーキーシステム)するかのいずれかを指定します。
<h4>番号</h4>
プレフィクス発信に使用できる番号は2つまでです。それぞれ内部名称がOGP1,OGP2となっています。
<h4>プレフィクス</h4>
プレフィクスに使用する番号を入力します。例えば0や9などです。
<h4>経路</h4>
NKS(トランク直接捕捉)またはキーを指定します。
<h4>経路番号</h4>
NKS時は"1"を入力してください。キー指定時にはキー番号を設定します。
<h4>発信CID</h4>
発信時に指定するCIDを指定します。ただし、先(トランク等の経路)で指定されているCIDがあ る場合には、そちらが優先されます。
<h4>市内局番</h4>
何も指定しない場合、ABSでは0で始まる番号しか発信できませんが、市内局番を指定すると市内局番に設定されている番号から始まる番号は発信できるようになります。例えば012345678はプレフィクス発信時には00312345678とダイヤルする必要がありますが、市内局番を"1"に設定すると012345678のように市街局番を省略してダイヤルすることが可能になります。
<h3>ノーキーシステム設定</h3>
プレフィクス発信するにはトランク捕捉のためにノーキーシステムを設定しておく必要があります。
<h4>番号</h4>
NKS1とNKS2の2つで、それぞれOGP1とOGP2に対応します。
<h4>TECH</h4>
トランクのテクノロジをPJSIPまたはSIPで指定します。
<h4>トランク</h4>
使用するトランクをプルダウンで選択するか、手動で入力でします。
<h4>種別</h4>
発信時のCID設定に必要な情報を指定するためのものです。ABSではNTT東西とBASIXでこの情報を使用します。必要ない場合には無指定(空白)にしておきます。
<h3>キー捕捉特番設定</h3>
特番でキーを捕捉するための設定です。
<h4>D56(*56[キー番号])特番発信</h4>
チャックを付けると*56または*57でキーを捕捉しての発信が可能となります。
<h4>D57(*57[番号])特番発信</h4>
<h4>番号</h4>
D57では1～4までの番号を指定できます(*571～*574)。
<h4>キー範囲</h4>
ハイフンで区切って使用するキーの範囲を指定します。例えば1に1-4と指定すると、*571をダイヤルすると、KEY1～KEY4の空きを探して使用します。空きは先頭(1)から順に探します。
<h3>トランク特殊設定</h3>
トランクで特殊な処理が必要な場合に設定する項目です。通常の回線の場合には必要ありません。
<h4>トランクスイッチャ</h4>
発信時のCIDによって発信するトランクを切り換える機能です。あるCIDでは、こっちのトランクを使うが別なCIDでは、あっちのトランクを使うといった場合に使用します。
<h4>トランクプレフィクス</h4>
プレフィクスを付けないと発信できないようなトランクの場合に指定します。

