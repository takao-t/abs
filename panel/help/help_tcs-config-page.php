<h3>時間外制御設定</h3>
営業時間外などの時間外制御を行います。
<h4>時間外制御</h4>
時間外の処理を選択します。<br>
時刻分岐しない：時間外制御を行わず通常着信します。<br>
時刻分岐あり：時間外にメッセージ再生や留守録を行います。<br>
強制時間外制御：現在時刻に関わらず時間外制御を行います。<br>
<h4>ダイヤルイン時制御</h4>
通常の時間外制御はキー着信時のみに適用されます。ダイヤルイン時にも時間外制御を行う場合にはこの項目で設定します。
<h4>着信可能時間</h4>
着信を許可する曜日、開始時刻、終了時刻を設定します。
<h4>祝日・休日制御</h4>
祝日・休日時の制御を行います。[祝日・休日管理]をクリックすることで祝日や休日として扱う日付を管理できます。
<h4>ダイヤルイン時休日制御</h4>
ダイヤルインでも休日処理を行うかどうかを設定します。
<h4>特番ダイヤルによるトグル切替</h4>
手動で"時間外"に設定する場合には特番をダイヤルすることで切替を行うことができます。この機能を使うかどうかを設定します。
<h4>時間制御切替用PIN</h4>
手動による時間外制御を行う場合のPINを設定します。
<h4>自動応答前待機時間</h4>
着信して速応答すると相手に伝わりにくいので何秒待機して応答するかを秒数で設定します。
<h4>時間外迂回機能</h4>
時間外制御を行っており、時間外応答メッセージの再生中にPIN番号を入力することで着信させる機能です。この機能を使うことで時間外であっても、PIN番号を知っている人は着信させることができます。着信はダイヤルインと同じ処理となります。キーでは着信できません。
