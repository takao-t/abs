<h3>内線グループ設定</h3>
内線をグループに所属させます。グループは着信先等として使用することがｄけいます。<br>
<h4>番号</h4>
グループ番号です。グループ着信等の着信先はこの番号となります。
<h4>所属内線</h4>
このグループに所属させる内線をカンマ区切りで入力します。
<h4>内線番号</h4>
グループに内線番号を割り当てる場合に入力します。内線にダイヤルすることでグループ呼出しが行えるようになります。
<h4>モード</h4>
着信時の鳴動モードを設定します。<br>
RA:一斉呼出-全内線を一斉に呼出します。
RR:順次呼出-先頭の内線から順番に呼出します。
RM:順次呼出(メモリあり)-順番に呼出しますが、次回の着信時には『次』の内線から呼出します。
<h4>タイムアウト</h4>
順次呼出しの場合。呼出し中の内線が応答しない場合に次にうつるまでの時間を秒で指定します。
<h4>話中検出</h4>
有効に設定すると、グループ内の内線を呼び出す際に、話中の内線を呼出し対象に含めません。
<h4>キュー</h4>
簡易コール・キューを使用するかどうかを指定します。キューが有効の場合、グループ内の内線がすべて話中であっても切断せずに呼出しを待機します。待機時間は[待機]で指定します。
<h4>待機</h4>
キュー使用時の再度呼び出すまでの待機時間を秒で指定します。
<h3>ピックアップグループ設定</h3>
ピックアップグループを設定します。
<h4>番号</h4>
ピックアップグループのグループ番号です。
<h4>所属内線</h4>
このグループに所属させる内線番号をカンマで区切って入力します。

