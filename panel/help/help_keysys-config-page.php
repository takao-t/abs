<h3>キーシステム設定</h3>
このページではキーシステムの設定を行います。キーシステムを使用しない場合には設定する必要はありません。
<h4>番号</h4>
キー番号です。
<h4>ラベル</h4>
キーに名前を付けます。このラベルはキー着信時に発信者名に"ラベル+数字"で表示されます。
<h4>TECH</h4>
キー発信時に使用するトランクのテクノロジを指定します。SIP(chan_sip)かPJSIP(chan_pjsip)で選択します。
<h4>トランク</h4>
キー発信時に使用するトランクを指定します。トランクが正しく設定されていればトランク名がプルダウンに表示されます。表示されないトランクを使用する場合には画面下の手動トランク設定でトランク名を指定して設定します。
<h4>種別</h4>
発信時のCID設定に必要な情報を指定するためのものです。ABSではNTT東西とBASIXでこの情報を使用します。必要ない場合には無指定(空白)にしておきます。
<h4>発信CID</h4>
発信時に指定するCIDを指定します。ただし、先(トランク等の経路)で指定されているCIDがある場合には、そちらが優先されます。
<h4>着信</h4>
キー着信時に着信させる先の内線またはグループを設定します。キーには着信させるものの、どの内線も鳴動させたくない場合にはシステム設定の『特殊内線(鳴動内線)設定』で鳴動のみする内線(仮想)を作成し、キーの着信先として鳴動内線をキーの着信先として指定してください。
<h4>RING</h4>
このキーに着信した際の鳴動パターンを設定します。
<h4>割込PIN</h4>
キーで通話中での割り込み時に指定するPINを設定します。PINが設定されていないキーには割り込みはできません。
<h4>割込MD</h4>
割り込みのモードを指定します。Bin(バージ:3者通話)またはSpy(スパイ:モニター)のいずれかです。
