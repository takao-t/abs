<h3>システム設定</h3>
各種のシステム情報を設定します。初期設定もこのページにあります。
<h4>各種情報確認</h4>
[ホスト情報確認]：このマシンの情報を表示します<br>
[内線情報確認]：設定した内線の情報、予約されている特番等を表示します<br>
[端末情報確認]：このABSに登録されているSIP端末(ピア)の情報を表示します<br>

<h4>ユーザ管理</h4>
WebUIを使用するユーザを管理します。ユーザ名とパスワードの設定を行います。<br>
<h4>内線テクノロジ</h4>
内線をPJSIPかSIPかどちらを使うのかを設定しますが、SIPはもはや推奨されません。
<h4>特殊内線設定</h4>
鳴動のみの内線を定義します。この内線はキー着信でどの電話機も鳴らしたくない場合に使用します。<br>
内線番号を割り当てることで着信先の内線として使用できますが、何もせず呼出のみが行われる内線になります。<br>
<h4>コールパーク呼び戻し時間</h4>
コールパーク時に自動的に呼び戻すまでの時間を設定します。
<h4>キー保留時点滅機能</h4>
キーシステム使用時、キーで保留している場合に保留しているキーを点滅させる機能です。ただしこの機能はSIP NOTIFYで点灯と点滅を繰り返して投げるので、必ずしもうまく動作するわけではないことに注意してください。点滅間隔は最低でも2秒以上にしてください。
<h4>エリア管理</h4>
PPIヘッダに使用するドメインを設定します。現在のところNTT東西とBASIX、ユーザ定義の4つまでです。
<h4>キーシステム初期化</h4>
キーシステムの状態を初期化します。通話中には実行しないでください。
<h4>ライセンスキー</h4>
割り当てられたライセンスキーを設定してください。
