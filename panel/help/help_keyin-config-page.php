<h3>キー着信設定</h3>
キー着信も番号ベースで着信を行います。どの番号をどのキーに着信させるかを設定します。<br>
着信先にはキー範囲を設定することができ、1-2のように指定すると2チャンネル使用することができます。
<h4>着信番号</h4>
着信させる番号を設定します。ハイフンやカッコを抜いた番号のみを設定します。
<h4>着信キー</h4>
着信先のキーを設定します。1や2のように単独キー、あるいは1-2や1-4のようにキー範囲を設定します。
<h4>着信時外線捕捉プレフィクス付加</h4>
着信時に外線捕捉用のプレフィクスを付加する方法を設定します。ダイヤルインで着信した場合にはOGP1で設定されたプレフィクスが付加されます。キー着信時にはキー発信用のプレフィクスが付加されます。<br>
*56は特定のキー位置を捕捉しますが、*57は指定された範囲のうち空いているキーを捕捉します。
