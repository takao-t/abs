<h3>フリーアドレスユーザ設定</h3>
フリーアドレスモードでは電話機からログインすることで内線番号を割り当てます。フリーアドレスで使用する電話機は専用の設定が必要ですので説明書を参照してください。<br>
フリーアドレス用に設定されている電話機からはユーザIDとパスワード(PIN)でログインすることで、内線番号等が割り当てられます。
<h4>ユーザ追加</h4>
フリーアドレスではユーザIDに内線番号が紐付きます。ユーザIDと内線番号を同じにしてもかまいませんが、内線番号は推察しやすいので別なものにすることをお勧めします。<br>
ユーザID：数字でユーザIDを指定します。<br>
PIN：ログイン時のパスワードですが数字のみで指定します。<br>
内線番号：このユーザに割り当てる内線番号を指定します。<br>
規制値：ABSの発信規制値です。<br>
発信者番号：発信CIDを明示指定する場合に設定します。内線に設定する発信CIDと同じ意味です。<br>
<h4>フリーアドレスユーザ一覧</h4>
設定されているユーザの一覧です。内容を変更したい場合には一旦、削除してから追加を行ってください。削除するにはチェックボックスをチェックして削除をクリックします。
<h4>他場所でログインしている場合の強制ログアウト</h4>
フリーアドレスの場合、すでに別の電話機でログインしているままなのに、さらに別な電話機からログインしようとする事象が発生します。このためデフォルトでは別な場所の電話機を強制ログアウトさせてから、現在操作している電話機でログインを実行します。この昨日を無効とし警告音声を流して切る(ログインさせない)場合には、この設定を「しない」にしてください。
